package org.ehcache.spi.service;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * Annotation that allows a {@link Service} implementation to declare an optional dependency on other {@code Service}s.
 */
@Retention(RetentionPolicy.RUNTIME)
@Target(ElementType.TYPE)
public @interface OptionalServiceDependencies {

  /**
   * Array of {@link Service} dependency classes
   *
   * @return the dependency class names
   */
  String[] value();
}
